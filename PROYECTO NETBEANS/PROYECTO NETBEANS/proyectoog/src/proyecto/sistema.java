// M√©todos para Ventas
public int registrarNuevaVenta(int clienteId, int vendedorId, double total) {
    try (Connection conn = ConexionMySQL.getConexion()) {
        String sql = "INSERT INTO ventas (cliente_id, vendedor_id, total) VALUES (?, ?, ?)";
        PreparedStatement pstmt = conn.prepareStatement(sql, Statement.RETURN_GENERATED_KEYS);
        pstmt.setInt(1, clienteId);
        pstmt.setInt(2, vendedorId);
        pstmt.setDouble(3, total);
        pstmt.executeUpdate();
        
        // Obtener el ID de la venta reci√©n creada
        ResultSet rs = pstmt.getGeneratedKeys();
        if (rs.next()) {
            return rs.getInt(1); // Retorna el ID de la venta
        }
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Error al registrar nueva venta: " + e.getMessage());
    }
    return -1; // Retorna -1 si hubo error
}

