    // M√©todos CRUD para Productos
public void agregarProducto(String codigo, String descripcion, int cantidad, double precio, int proveedorId) {
    Connection conn = null;
    try {
        conn = ConexionMySQL.getConexion();
        conn.setAutoCommit(false); // Desactivar autocommit
        
        String sql = "INSERT INTO productos (codigo, descripcion, cantidad, precio, proveedor_id) VALUES (?, ?, ?, ?, ?)";
        PreparedStatement pstmt = conn.prepareStatement(sql);
        pstmt.setString(1, codigo);
        pstmt.setString(2, descripcion);
        pstmt.setInt(3, cantidad);
        pstmt.setDouble(4, precio);
        pstmt.setInt(5, proveedorId);
        pstmt.executeUpdate();
        
        conn.commit(); // Confirmar la transacci√≥n
        JOptionPane.showMessageDialog(this, "Producto agregado exitosamente");
        actualizarTablaProductos();
    } catch (SQLException e) {
        try {
            if(conn != null) conn.rollback(); // Revertir en caso de error
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
        JOptionPane.showMessageDialog(this, "Error al agregar producto: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    } finally {
        try {
            if(conn != null) conn.setAutoCommit(true); // Restaurar autocommit
            if(conn != null) conn.close();
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }
}

