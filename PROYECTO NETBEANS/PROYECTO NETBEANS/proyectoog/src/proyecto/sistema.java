private boolean validarCamposProducto(String codigo, String descripcion, int cantidad, double precio) {
    if(codigo.isEmpty() || descripcion.isEmpty()) {
        JOptionPane.showMessageDialog(this, "C√≥digo y descripci√≥n son obligatorios", "Validaci√≥n", JOptionPane.WARNING_MESSAGE);
        return false;
    }
    if(cantidad <= 0 || precio <= 0) {
        JOptionPane.showMessageDialog(this, "Cantidad y precio deben ser mayores a cero", "Validaci√≥n", JOptionPane.WARNING_MESSAGE);
        return false;
    }
    return true;
}

